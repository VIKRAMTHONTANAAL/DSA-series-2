package bitwise2025;

public class AndOrNotXor {


    public static void main(String[] args) {
        int a =4, b=6;

        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(~a+" "+~b);
        System.out.println(a^b);

        System.out.println(17>>1);
        System.out.println(17>>2);
        System.out.println(19<<1);
        System.out.println(21<<2);
    }
}
